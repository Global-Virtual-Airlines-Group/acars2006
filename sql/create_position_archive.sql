CREATE TABLE POSITION_ARCHIVE (
 FLIGHT_ID			INTEGER UNSIGNED NOT NULL,
 REPORT_TIME		DATETIME NOT NULL,
 TIME_MS			SMALLINT UNSIGNED NOT NULL,
 LAT				DECIMAL (9,4) NOT NULL,
 LNG				DECIMAL (9,4) NOT NULL,
 B_ALT				INTEGER,
 R_ALT				INTEGER UNSIGNED,
 HEADING			SMALLINT,
 PITCH				DECIMAL (7,4) DEFAULT 0,
 BANK				DECIMAL (7,4) DEFAULT 0,
 ASPEED				SMALLINT,
 GSPEED				SMALLINT,
 VSPEED				SMALLINT,
 N1					DECIMAL (4,1) UNSIGNED,
 N2					DECIMAL (4,1) UNSIGNED,
 MACH				DECIMAL (5,3) UNSIGNED,
 FUEL				INTEGER UNSIGNED,
 PHASE				TINYINT,
 SIM_RATE    		SMALLINT,
 FLAPS				TINYINT,
 FLAGS				INTEGER UNSIGNED,
 WIND_HDG			SMALLINT UNSIGNED DEFAULT 0,
 WIND_SPEED			SMALLINT UNSIGNED DEFAULT 0,
 FUELFLOW			INTEGER UNSIGNED DEFAULT 0,
 AOA				DECIMAL (5,3) DEFAULT 0,
 GFORCE				DECIMAL (5,3) DEFAULT 1,
 FRAMERATE			SMALLINT UNSIGNED DEFAULT 0,
 PRIMARY KEY (FLIGHT_ID, REPORT_TIME, TIME_MS),
 FOREIGN KEY (FLIGHT_ID) REFERENCES FLIGHTS(ID) ON DELETE CASCADE
);