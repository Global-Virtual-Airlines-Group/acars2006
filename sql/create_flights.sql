CREATE TABLE FLIGHTS (
 ID				INTEGER UNSIGNED NOT NULL AUTO_INCREMENT,
 CON_ID			BIGINT UNSIGNED NOT NULL,
 CREATED		DATETIME NOT NULL,
 END_TIME		DATETIME,
 FLIGHT_NUM		VARCHAR(14) NOT NULL,
 EQTYPE			VARCHAR(16) NOT NULL,
 CRUISE_ALT		VARCHAR(8),
 AIRPORT_D		CHAR(3) NOT NULL,
 AIRPORT_A		CHAR(3) NOT NULL,
 AIRPORT_L		CHAR(3),
 ROUTE			VARCHAR(255),
 REMARKS		VARCHAR(255),
 FSVERSION		SMALLINT NOT NULL,
 OFFLINE		BOOL,
 PRIMARY KEY (ID),
 FOREIGN KEY (CON_ID) REFERENCES CONS(id),
 FOREIGN KEY (AIRPORT_D) REFERENCES common.AIRPORTS(IATA),
 FOREIGN KEY (AIRPORT_A) REFERENCES common.AIRPORTS(IATA)
);