CREATE TABLE POSITIONS (
 CON_ID				INTEGER UNSIGNED NOT NULL,
 FLIGHT_ID			INTEGER UNSIGNED NOT NULL,
 REPORT_TIME		TIMESTAMP,
 LAT				DECIMAL (9,4) NOT NULL,
 LNG				DECIMAL (9,4) NOT NULL,
 B_ALT				INTEGER UNSIGNED,
 R_ALT				INTEGER UNSIGNED,
 HEADING			SMALLINT UNSIGNED,
 ASPEED				SMALLINT UNSIGNED,
 GSPEED				SMALLINT UNSIGNED,
 VSPEED				SMALLINT,
 N1					DECIMAL (4,1) UNSIGNED,
 N2					DECIMAL (4,1) UNSIGNED,
 MACH				DECIMAL(5, 3) UNSIGNED,
 FUEL				INTEGER UNSIGNED,
 PHASE				TINYINT UNSIGNED,
 SIM_RATE    		SMALLINT UNSIGNED,
 FLAGS				INTEGER UNSIGNED,
 PRIMARY KEY (CON_ID, REPORT_TIME),
 FOREIGN KEY (CON_ID) REFERENCES CONS(ID) ON DELETE CASCADE,
 FOREIGN KEY (FLIGHT_ID) REFERENCES FLIGHTS(ID) ON DELETE CASCADE
);
