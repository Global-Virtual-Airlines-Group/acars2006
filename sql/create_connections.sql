CREATE TABLE CONS (
 ID					BIGINT UNSIGNED NOT NULL,
 PILOT_ID			INTEGER UNSIGNED NOT NULL,
 DATE				DATETIME NOT NULL,
 REMOTE_ADDR		INTEGER UNSIGNED NOT NULL,
 REMOTE_HOST    	VARCHAR(128) NOT NULL,
 CLIENT_BUILD		SMALLINT NOT NULL DEFAULT 0,
 BETA_BUILD			SMALLINT NOT NULL DEFAULT 0,
 DISPATCH			BOOLEAN NOT NULL DEFAULT 0,
 PRIMARY KEY(ID),
 FOREIGN KEY (PILOT_ID) REFERENCES common.USERDATA(ID)
);